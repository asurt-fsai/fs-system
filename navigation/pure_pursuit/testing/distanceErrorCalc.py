import numpy as np
from scipy.interpolate import interp1d
from typing import List, Tuple


def calculate_error(
    actual_trajectory: List[Tuple[float, float]], desired_trajectory: List[Tuple[float, float]]
) -> float:
    # Interpolate the longer trajectory if necessary
    if len(actual_trajectory) > len(desired_trajectory):
        x_actual = [t[0] for t in actual_trajectory]
        y_actual = [t[1] for t in actual_trajectory]
        f_actual = interp1d(x_actual, y_actual)
        x_desired = [t[0] for t in desired_trajectory]
        y_desired = f_actual(x_desired)
        interpolated_actual = list(zip(x_desired, y_desired))
        actual_trajectory = interpolated_actual
    elif len(desired_trajectory) > len(actual_trajectory):
        x_desired = [t[0] for t in desired_trajectory]
        y_desired = [t[1] for t in desired_trajectory]
        f_desired = interp1d(x_desired, y_desired)
        x_actual = [t[0] for t in actual_trajectory]
        y_actual = f_desired(x_actual)
        interpolated_desired = list(zip(x_actual, y_actual))
        desired_trajectory = interpolated_desired

    # Calculate the error as the sum of Euclidean distances
    error = 0
    for actual_point, desired_point in zip(actual_trajectory, desired_trajectory):
        x_actual, y_actual = actual_point
        x_desired, y_desired = desired_point
        distance = np.sqrt((x_actual - x_desired) ** 2 + (y_actual - y_desired) ** 2)
        error += distance
    return error


